var pos;
var dir;
var plane;
var rot;
var rotSpeed;
var walkSpeed;

var lastTime;

function setup() {
  createCanvas(800, 560);
  pos = createVector(12,12);
  dir = createVector(0,1);
  plane = createVector(1,0);
  rot = 0;
  rotSpeed = 0.1;
  walkSpeed = 0.015;
}

function draw() {
  
  var dt = millis() - lastTime;
  update(dt);
  drawMap();
  console.log(frameRate());
  lastTime = millis();
}

function update(dt) {
  dir.x = cos(rot);
  dir.y = -sin(rot);  //play with these to really screw things up
  // plane.x = sin(rot);
  // plane.y = cos(rot);
  
  if (keyIsDown(LEFT_ARROW)) {
    rot += rotSpeed;
  }
  
  if (keyIsDown(RIGHT_ARROW)) {
    rot -= rotSpeed;
  }
  
  if (keyIsDown(UP_ARROW)) {
    if (worldMap[floor(pos.x + dir.x * walkSpeed * dt)][floor(pos.y)] === 0) {
      pos.x += dir.x * walkSpeed * dt;
    }
    if (worldMap[floor(pos.x)][floor(pos.y + dir.y * walkSpeed * dt)] === 0) {
      pos.y += dir.y * walkSpeed * dt;
    }
  }
  
  if (keyIsDown(DOWN_ARROW)) {
    if (worldMap[floor(pos.x - dir.x * walkSpeed * dt)][floor(pos.y)] === 0) {
      pos.x -= dir.x * walkSpeed * dt;
    }
    if (worldMap[floor(pos.x)][floor(pos.y - dir.y * walkSpeed * dt)] === 0) {
      pos.y -= dir.y * walkSpeed * dt;
    }
  }
  
}

function drawMap() {
  background(255);
  for (var i = 0; i < worldMap.length; i++) {
    for (var j = 0; j < worldMap[0].length; j++) {
      if (worldMap[i][j] === 0) {
        fill(255);
        // stroke(0);
        rect(i * 20, j * 20, 20, 20);
      } else {
        fill(0);
        noStroke();
        rect(i * 20, j * 20, 20, 20);
      }
      
      
    }
  }
  stroke(0);
  fill(255,255,0);
  ellipse(pos.x * 20, pos.y * 20, 20, 20);
  
  
  line(pos.x * 20, pos.y * 20, (pos.x + dir.x) * 20, (pos.y + dir.y) * 20);
}